// 수동 번역 데이터 (제목 + 설명 + 조리법)
// 자동 생성 불가능한 고유 번역을 여기에 기록
// ──────────────────────────────────────────
import { MANUAL_TRANSLATIONS } from './maff-translations-manual';

// ──────────────────────────────────────────
// 메인 처리
// ──────────────────────────────────────────
export interface MaffTranslatedRecipe {
  slug: string;
  prefecture: string;
  prefectureKo: string;
  title: string;
  description: string;
  dishType: string;
  season: string;
  servings: number;
  images: string[];
  ingredients: Array<{ name: string; amount: string }>;
  steps: string[];
  url: string;
}

async function main() {
  const args = process.argv.slice(2);
  const startIdx = parseInt(args[0] ?? '0', 10);
  const endIdx = parseInt(args[1] ?? '100', 10);

  const raw: any[] = JSON.parse(fs.readFileSync(RAW_FILE, 'utf-8'));
  const batch = raw.slice(startIdx, endIdx);

  // 기존 번역 파일 로드
  let existing: MaffTranslatedRecipe[] = [];
  if (fs.existsSync(OUT_FILE)) {
    existing = JSON.parse(fs.readFileSync(OUT_FILE, 'utf-8'));
  }
  const existingSlugs = new Set(existing.map(r => r.slug));

  const results: MaffTranslatedRecipe[] = [...existing];
  let added = 0;

  for (const r of batch) {
    if (existingSlugs.has(r.slug)) continue;

    const manual = MANUAL_TRANSLATIONS[r.slug];
    const prefectureKo = PREFECTURE_KO[r.prefecture] ?? r.prefecture;

    const translated: MaffTranslatedRecipe = {
      slug: r.slug,
      prefecture: r.prefecture,
      prefectureKo,
      title: manual?.title ?? r.nameKanji,  // 수동 번역 없으면 원본 유지
      description: manual?.description ?? '',
      dishType: r.dishType,
      season: r.season,
      servings: parseInt(r.servings?.match(/\d+/)?.[0] ?? '2', 10),
      images: r.images ?? [],
      ingredients: r.ingredients.map((ing: any) => ({
        name: translateIngredientName(ing.name),
        amount: translateAmount(ing.amount ?? ''),
      })),
      steps: manual?.steps ?? r.instructions,  // 수동 번역 없으면 원본(일본어) 유지
      url: r.url,
    };

    results.push(translated);
    existingSlugs.add(r.slug);
    added++;
    console.log(`✅ ${r.prefecture} - ${r.nameKanji} → ${translated.title}`);
  }

  fs.writeFileSync(OUT_FILE, JSON.stringify(results, null, 2));
  console.log(`\n완료! 추가: ${added}개 (총 ${results.length}개)`);
}

main().catch(console.error);
