function translateIngredientName(jpName: string): string {
  // 【A】 등 그룹 표시 제거
  let name = jpName.replace(/^【[^】]*】\s*/, '').replace(/^\[[^\]]*\]\s*/, '').trim();
  if (!name) return jpName;

  // Stage 1: ING_DICT — 재료명 번역 (첫 번째 매칭)
  for (const [pattern, ko] of ING_DICT) {
    if (typeof pattern === 'string') {
      if (name.includes(pattern)) { name = name.replace(pattern, ko); break; }
    } else {
      if (pattern.test(name)) { name = name.replace(pattern, ko); break; }
    }
  }

  // Stage 2: PREP_DICT — 조리법·상태 전역 치환 (남은 일본어 처리)
  if (/[぀-ゟ゠-ヿ一-鿿]/.test(name)) {
    for (const [pattern, ko] of PREP_DICT) {
      name = name.replace(pattern, ko);
    }
    name = name.trim();
  }

  return name;
}

function translateAmount(amount: string): string {
  return amount
    .replace(/大さじ/g, '큰술')
    .replace(/小さじ/g, '작은술')
    .replace(/適量/g, '적당량')
    .replace(/少々/g, '약간')
    .replace(/ひとつまみ/g, '한 꼬집')
    .replace(/カップ/g, '컵')
    .replace(/お好みで/g, '취향껏')
    .replace(/人分/g, '인분')
    .replace(/個分/g, '개분')
    .replace(/本分/g, '개분');
}

