async function main() {
  const supabase = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY, {
    auth: { autoRefreshToken: false, persistSession: false },
  });

  console.log(`📦 총 ${RECIPES.length}개 레시피 재료 삽입 시작\n`);

  let success = 0;
  let failed = 0;

  for (const recipe of RECIPES) {
    // description 업데이트
    const { error: descErr } = await supabase
      .from('recipes')
      .update({ description: recipe.description })
      .eq('id', recipe.id);

    if (descErr) {
      console.log(`❌ description 업데이트 실패 (${recipe.id}): ${descErr.message}`);
    }

    // 재료 삽입
    const rows = recipe.ingredients.map((ing, idx) => ({
      recipe_id: recipe.id,
      ingredient_name: ing.n,
      quantity: ing.q,
      unit: ing.u,
      notes: ing.o ?? null,
      is_optional: false,
      display_order: idx + 1,
    }));

    const { error: ingErr } = await supabase
      .from('recipe_ingredients')
      .insert(rows);

    if (ingErr) {
      console.log(`❌ 재료 삽입 실패 (${recipe.id}): ${ingErr.message}`);
      failed++;
    } else {
      console.log(`✅ 재료 ${rows.length}개 삽입 완료`);
      success++;
    }
  }

  console.log(`\n──────────────────────────`);
  console.log(`✅ 성공: ${success}개`);
  console.log(`❌ 실패: ${failed}개`);
  console.log(`──────────────────────────\n`);
}

main();
