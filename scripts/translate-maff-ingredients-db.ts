function translateNotes(text: string): string {
  if (!text) return text;
  let s = text;

  // notes 필드에도 재료명 사전 적용 (괄호 안 재료명 번역)
  for (const [jp, ko] of ING_MAP) {
    s = s.replaceAll(jp, ko);
  }

  // 大さじ/小さじ 먼저 (SIZE_MAP)
  for (const [jp, ko] of SIZE_MAP) {
    s = s.replaceAll(jp, ko);
  }

  // 구/문장 치환
  for (const [jp, ko] of PHRASE_MAP) {
    s = s.replaceAll(jp, ko);
  }

  // 숫자 + 단위 치환 (예: 1枚 → 1장)
  for (const [jp, ko] of UNITS) {
    // 숫자·분수·소수 뒤의 단위
    s = s.replace(
      new RegExp(`(\\d+(?:[./]\\d+)?)\\s*${escapeRe(jp)}`, 'g'),
      `$1${ko}`
    );
    // 단독 단위 (앞에 숫자 없어도 단위로 쓰인 경우)
    s = s.replace(
      new RegExp(`(?<=[\\u0020\\u0028（])${escapeRe(jp)}(?=[\\s\\u0029）,，、。\\n]|$)`, 'g'),
      ko
    );
  }

  // 크기 접두사 (小1本 → 소1개)
  for (const [jp, ko] of SIZE_PREFIX) {
    s = s.replace(new RegExp(`^${escapeRe(jp)}(\\d)`, 'g'), `${ko}$1`);
    s = s.replace(new RegExp(`([（(\\s])${escapeRe(jp)}(\\d)`, 'g'), `$1${ko}$2`);
  }

  // XcmY角 → XcmY 크기 (예: 10cm角 → 10cm 크기)
  s = s.replace(/(\d+(?:\.\d+)?)\s*cm\s*角/g, '$1cm 크기');
  s = s.replace(/(\d+(?:\.\d+)?)\s*mm\s*幅/g, '$1mm 너비');
  // X人前 → X인분
  s = s.replace(/(\d+)\s*人前/g, '$1인분');
  s = s.replace(/(\d+)\s*人分/g, '$1인분');
  // と → 과 (숫자 사이)
  s = s.replace(/(\d)と(\d)/g, '$1과 $2');
  s = s.replace(/(\d)・(\d)/g, '$1과 $2');
  // ／ → /
  s = s.replaceAll('／', '/');
  // 강/약 모디파이어
  s = s.replace(/(\d)\s*強(?=[^力]|$)/g, '$1 강');
  s = s.replace(/(\d)\s*弱(?=[^点]|$)/g, '$1 약');
  // 컵杯 → 컵 (杯 중복 제거)
  s = s.replace(/컵(\d+)杯/g, '컵$1');
  s = s.replace(/컵(\d*)杯/g, '컵$1');
  s = s.replace(/큰술(\d+)杯/g, '큰술$1');
  s = s.replace(/작은술(\d+)杯/g, '작은술$1');
  // 残余 杯 → 잔
  s = s.replace(/(\d+)\s*杯/g, '$1잔');

  // 한국어 단위 뒤 弱/強 처리 (예: 1/2컵弱 → 1/2컵 약간)
  s = s.replace(/(컵|큰술|작은술|봉지|개|장|팩|ml|L|g|kg)\s*弱/g, '$1 약간');
  s = s.replace(/(컵|큰술|작은술|봉지|개|장|팩|ml|L|g|kg)\s*強/g, '$1 강');

  // 일본어 파티클 (한국어/숫자 사이에서만 처리)
  s = s.replace(/([가-힣\d])\s*と\s*([가-힣])/g, '$1와 $2');
  s = s.replace(/([가-힣\d])\s*を\s*([가-힣])/g, '$1을 $2');
  s = s.replace(/([가-힣])が([가-힣])/g, '$1이 $2');
  s = s.replace(/([가-힣])に([가-힣])/g, '$1에 $2');

  // の after Korean/number → 의 (다양한 맥락에서)
  s = s.replace(/([가-힣\d)])\s*の/g, '$1의 ');

  return s.trim();
}

function translateIngName(name: string): string {
  if (!name || !hasJapanese(name)) return name;
  let s = name;
  // 재료명 사전
  for (const [jp, ko] of ING_MAP) {
    if (s === jp) return ko;
  }
  for (const [jp, ko] of ING_MAP) {
    s = s.replaceAll(jp, ko);
  }
  // 그래도 일본어 남으면 notes 번역 로직도 적용
  if (hasJapanese(s)) {
    s = translateNotes(s);
  }
  return s;
}

function escapeRe(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

