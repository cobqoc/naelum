async function sleep(ms: number) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function geminiTranslate(items: string[]): Promise<Map<string, string>> {
  const prompt = `일본어가 포함된 식재료명 또는 수량 표현입니다. 한국어로 번역하세요.
이미 한국어가 있으면 일본어 부분만 한국어로 바꾸세요.
결과는 입력과 동일한 순서의 JSON 배열로만 반환하세요. 설명 없이.

입력:
${JSON.stringify(items, null, 2)}

출력 (JSON 배열만):`;

  const res = await fetch(GEMINI_URL, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      contents: [{ parts: [{ text: prompt }] }],
      generationConfig: { temperature: 0.1, maxOutputTokens: 4096 },
    }),
  });

  if (!res.ok) throw new Error(`Gemini ${res.status}: ${(await res.text()).slice(0, 200)}`);

  const json = await res.json() as any;
  const text: string = json.candidates?.[0]?.content?.parts?.[0]?.text ?? '';
  const m = text.match(/\[[\s\S]*\]/);
  if (!m) throw new Error('JSON 없음: ' + text.slice(0, 200));

  const translated: string[] = JSON.parse(m[0]);
  if (translated.length !== items.length) throw new Error(`길이 불일치: ${items.length} → ${translated.length}`);

  const map = new Map<string, string>();
  items.forEach((item, i) => map.set(item, translated[i] ?? item));
  return map;
}

