async function main() {
  console.log(`모드: ${DRY_RUN ? '드라이런' : PROD ? 'PROD' : 'DEV'}`);

  // 1. MAFF 레시피 ID 수집
  console.log('MAFF 레시피 ID 수집 중...');
  const maffIds: string[] = [];
  let from = 0;
  while (true) {
    const { data, error } = await supabase
      .from('recipes')
      .select('id')
      .like('source_url', '%maff.go.jp%')
      .range(from, from + 999);
    if (error) { console.error(error); process.exit(1); }
    if (!data?.length) break;
    maffIds.push(...data.map((r: any) => r.id));
    if (data.length < 1000) break;
    from += 1000;
  }
  console.log(`MAFF 레시피: ${maffIds.length}개`);

  // 2. recipe_ingredients 전체 조회 (배치)
  console.log('재료 데이터 조회 중...');
  const allRows: Array<{ id: string; ingredient_name: string | null; notes: string | null }> = [];
  const CHUNK = 100;

  for (let i = 0; i < maffIds.length; i += CHUNK) {
    const chunk = maffIds.slice(i, i + CHUNK);
    let offset = 0;
    while (true) {
      const { data, error } = await supabase
        .from('recipe_ingredients')
        .select('id, ingredient_name, notes')
        .in('recipe_id', chunk)
        .range(offset, offset + 999);
      if (error) { console.error(error); break; }
      if (!data?.length) break;
      allRows.push(...data);
      if (data.length < 1000) break;
      offset += 1000;
    }
    process.stdout.write(`\r  ${Math.min(i + CHUNK, maffIds.length)}/${maffIds.length} 레시피 처리`);
  }
  console.log(`\n총 재료 행: ${allRows.length}개`);

  // 3. 일본어 포함 행 필터
  const jpRows = allRows.filter(r => hasJapanese(r.ingredient_name) || hasJapanese(r.notes));
  console.log(`일본어 포함: ${jpRows.length}개`);

  // 4. 사전으로 1차 번역
  interface UpdateRow {
    id: string;
    ingredient_name?: string;
    notes?: string;
    needsGemini_name?: string;
    needsGemini_notes?: string;
  }

  const updates: UpdateRow[] = [];
  let dictSolved = 0;
  let geminiCount = 0;

  for (const row of jpRows) {
    const u: UpdateRow = { id: row.id };

    if (hasJapanese(row.ingredient_name)) {
      const translated = translateIngName(row.ingredient_name!);
      if (translated !== row.ingredient_name) {
        u.ingredient_name = translated;
        if (!hasJapanese(translated)) dictSolved++;
        else { u.needsGemini_name = translated; geminiCount++; }
      } else {
        u.needsGemini_name = row.ingredient_name!;
        geminiCount++;
      }
    }

    if (hasJapanese(row.notes)) {
      const translated = translateNotes(row.notes!);
      if (translated !== row.notes) {
        u.notes = translated;
        if (!hasJapanese(translated)) dictSolved++;
        else { u.needsGemini_notes = translated; geminiCount++; }
      } else {
        u.needsGemini_notes = row.notes!;
        geminiCount++;
      }
    }

    updates.push(u);
  }

  console.log(`사전 해결: ${dictSolved}개 | Gemini 필요: ${geminiCount}개`);

  // 5. Gemini로 2차 번역
  if (geminiCount > 0 && GEMINI_API_KEY && !DRY_RUN && !DICT_ONLY) {
    const uniqueTexts = [...new Set([
      ...updates.flatMap(u => u.needsGemini_name ? [u.needsGemini_name] : []),
      ...updates.flatMap(u => u.needsGemini_notes ? [u.needsGemini_notes] : []),
    ])];
    console.log(`Gemini 고유 텍스트: ${uniqueTexts.length}개`);

    const cache = new Map<string, string>();
    const BATCH = 40;

    for (let i = 0; i < uniqueTexts.length; i += BATCH) {
      const batch = uniqueTexts.slice(i, i + BATCH);
      const batchNum = Math.floor(i / BATCH) + 1;
      const total = Math.ceil(uniqueTexts.length / BATCH);
      process.stdout.write(`Gemini 배치 ${batchNum}/${total}... `);

      for (let attempt = 0; attempt < 3; attempt++) {
        try {
          const map = await geminiTranslate(batch);
          map.forEach((v, k) => cache.set(k, v));
          console.log('✅');
          break;
        } catch (e) {
          const msg = (e as Error).message;
          const is429 = msg.includes('429');
          if (attempt >= 2) { console.log(`❌ ${msg.slice(0, 80)}`); break; }
          const wait = is429 ? 90000 : 6000;
          console.log(`⚠️ 재시도 (${Math.round(wait / 1000)}초)...`);
          await sleep(wait);
        }
      }
      if (i + BATCH < uniqueTexts.length) await sleep(5000);
    }

    // 캐시 결과를 updates에 반영
    for (const u of updates) {
      if (u.needsGemini_name) {
        const t = cache.get(u.needsGemini_name);
        if (t) u.ingredient_name = t;
      }
      if (u.needsGemini_notes) {
        const t = cache.get(u.needsGemini_notes);
        if (t) u.notes = t;
      }
    }
  }

  // 실제로 변경이 있는 것만
  const toUpdate = updates.filter(u => u.ingredient_name !== undefined || u.notes !== undefined);
  console.log(`\n최종 업데이트 대상: ${toUpdate.length}개`);

  if (DRY_RUN) {
    console.log('\n[드라이런] 사전 치환 샘플 (처음 20개):');
    toUpdate.slice(0, 20).forEach(u => {
      const orig = jpRows.find(r => r.id === u.id);
      if (u.ingredient_name !== undefined) {
        console.log(`  재료명: "${orig?.ingredient_name}" → "${u.ingredient_name}"`);
      }
      if (u.notes !== undefined) {
        console.log(`  수량:   "${orig?.notes}" → "${u.notes}"`);
      }
    });

    // Gemini 필요 항목 샘플
    const geminiItems = updates.filter(u => u.needsGemini_name || u.needsGemini_notes);
    console.log(`\n[드라이런] Gemini 필요 항목 샘플 (처음 30개):`);
    const seen = new Set<string>();
    let shown = 0;
    for (const u of geminiItems) {
      if (shown >= 30) break;
      if (u.needsGemini_name && !seen.has(u.needsGemini_name)) {
        seen.add(u.needsGemini_name);
        console.log(`  재료명: "${u.needsGemini_name}"`);
        shown++;
      }
      if (u.needsGemini_notes && !seen.has(u.needsGemini_notes)) {
        seen.add(u.needsGemini_notes);
        console.log(`  수량:   "${u.needsGemini_notes}"`);
        shown++;
      }
    }
    console.log('\n--- 드라이런 완료 ---');
    return;
  }

  // 6. DB 업데이트
  console.log('\nDB 업데이트 중...');
  let updatedCount = 0;
  const UPDATE_BATCH = 50;

  for (let i = 0; i < toUpdate.length; i += UPDATE_BATCH) {
    const batch = toUpdate.slice(i, i + UPDATE_BATCH);
    await Promise.all(batch.map(async u => {
      const updateData: Record<string, string> = {};
      if (u.ingredient_name !== undefined) updateData.ingredient_name = u.ingredient_name;
      if (u.notes !== undefined) updateData.notes = u.notes;

      const { error } = await supabase
        .from('recipe_ingredients')
        .update(updateData)
        .eq('id', u.id);

      if (error) console.error(`ID ${u.id} 실패:`, error.message);
      else updatedCount++;
    }));
    process.stdout.write(`\r  ${Math.min(i + UPDATE_BATCH, toUpdate.length)}/${toUpdate.length}`);
  }

  console.log(`\n\n✅ 완료: ${updatedCount}개 업데이트`);
}

main().catch(console.error);
