// 보편 재료 — 수돗물처럼 누구나 항상 갖고 있는 것으로 가정.
// 매칭에서 제외해서 ready/missing 계산을 왜곡하지 않도록 처리.
export const FUNDAMENTAL_INGREDIENTS = new Set(['물', '생수', '식수', 'water'])

export function isFundamental(name: string): boolean {
  const n = name.toLowerCase().trim()
  return FUNDAMENTAL_INGREDIENTS.has(n)
}

export function isIngredientMatch(userIng: string, recipeIng: string): boolean {
  const a = userIng.toLowerCase().trim()
  const b = recipeIng.toLowerCase().trim()

  // 정확히 일치
  if (a === b) return true

  // 조리법 접두사 정규화 후 비교 (다진파→대파, 채썬양파→양파, 다진마늘→마늘 등)
  const na = normalizeIngredientName(a)
  const nb = normalizeIngredientName(b)
  if (na === nb) return true

  // 동의어 체크 — user 기준(a) + recipe 기준(b) 양방향으로 조회
  const synonymsA = INGREDIENT_SYNONYMS[a] ?? INGREDIENT_SYNONYMS[na]
  if (synonymsA && synonymsA.some(s => s === b || s === nb || b.includes(s) || s.includes(b))) return true
  // recipe 기준 역방향 — substring 포함 체크 금지 (쪽파⊃파 → 대파 오매칭 방지)
  const synonymsB = INGREDIENT_SYNONYMS[b] ?? INGREDIENT_SYNONYMS[nb]
  if (synonymsB && synonymsB.some(s => s === a || s === na)) return true

  // ⚠️ prefix 부분일치 규칙 제거(2026-05-17): '고추'가 '고추장'의 접두라는
  // 이유로 장류↔생고추를 오매칭하던 버그. 간장↔간, 김↔김치 등 의미가
  // 완전히 다른 재료를 광범위 오매칭. 정당한 부분일치(다진마늘↔마늘,
  // 대파↔파 등)는 INGREDIENT_SYNONYMS 가 커버하므로 동의어 테이블을
  // 단일 진실원천으로 삼는다. 매칭은 exact + synonym + FK + Levenshtein 만.

  // 레벤슈타인 유사도 (0.7 이상이면 매칭)
  if (a.length >= 2 && b.length >= 2) {
    const similarity = levenshteinSimilarity(a, b)
    if (similarity >= 0.7) return true
  }

  return false
}

// 추천 mode 분류 술어 — recipesWithMatch 항목에 대해 평가.
export const isReady = (r: { matchedCount: number; totalIngredients: number }) =>
  r.totalIngredients > 0 && r.matchedCount === r.totalIngredients
// '거의 가능' = 부족 1~2개 AND 이미 대부분 보유(matchRate ≥ 70%).
// 절대기준(부족 ≤2)만 쓰면 3재료 레시피에서 1개(33%)만 있어도 '거의'로
// 오분류됨(단감피클·우무오미자냉화채 사례). "1~2개만 더 사면 OK"라는
// 약속은 이미 대부분을 가졌을 때만 참이다.
// 임계값 70%: N=3 레시피는 ready 가 아니면 '거의 가능' 불가(2/3=67%<70),
// N=4 는 3/4, N=5 는 4/5 부터. 사용자 결정(2026-05-17, 잡추천 최소화).
export const isAlmost = (r: { missingCount: number; matchRate: number }) =>
  r.missingCount >= 1 && r.missingCount <= 2 && r.matchRate >= 70
export const isAny = (r: { matchRate: number }) => r.matchRate > 0
